#include "libft.h"

char	*ft_strchr(const char *s1, int c)
{
	if (c > 127)
		return ((char *)s1);
	while (*s1)
	{
		if (*s1 == c)
			return ((char *)(s1));
		s1++;
	}
	if (c == '\0')
		return ((char *)s1);
	return ((char *)0);
}
